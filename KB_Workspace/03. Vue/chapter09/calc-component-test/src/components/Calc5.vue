<template>
  <div>
    X: <input type="text" v-model.number="x" /><br />
    <div>결과 : {{ result }}</div>
  </div>
</template>

<script>
import { ref, watch } from 'vue';

export default {
  name: 'Calc5',
  setup() {
    const x = ref(0);
    const result = ref(0);
    watch(x, (current, old) => {
      // current와 old는 ref의 객체가 아닌 ref.value 값이다.
      console.log(`${old} -> ${current}`);
      result.value = current * 2;
    });

    return { x, result };
  },
};
</script>
